async function main() {
  console.log('🌱 Seeding SAT mock tests...')

  await prisma.certificate.deleteMany()
  await prisma.testResult.deleteMany()
  await prisma.question.deleteMany()
  await prisma.practiceTest.deleteMany()

  for (const test of tests) {
    const { questions, ...testData } = test
    const created = await prisma.practiceTest.create({
      data: {
        ...testData,
        questions: {
          create: questions.map((q, i) => ({ ...q, order: i })),
        },
      },
    })
    console.log(`✅ ${created.title}`)
  }

  console.log(`\n✨ Done — ${tests.length} SAT mock tests seeded.`)
}

main()
  .catch(console.error)
  .finally(() => prisma.$disconnect())
